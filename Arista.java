public class Arista {
    private Nodo origen;
    private Nodo destino;

    public Nodo obtenerOrigen() {
        return origen;
    }

    public void establecerOrigen(Nodo origen) {
        this.origen = origen;
    }

    public Nodo obtenerDestino() {
        return destino;
    }

    public void establecerDestino(Nodo destino) {
        this.destino = destino;
    }
}

